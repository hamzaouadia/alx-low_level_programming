#include <stdio.h>

/**
 * main - putchar
 *
 * Description: program that prints the lowercase alphabet in reverse
 *	followed by a new line.
 * Return: always 0.
 */
int main(void)
{
	char	*str;
	int 	c;

	str[8] = "ebahpla";
	c = 7;
	while (c <= 0)
	{
		putchar(str[c]);
		c--;
	}
	putchar('\n');
	return (0);
}
