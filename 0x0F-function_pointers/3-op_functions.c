#include "3-calc.h"

/**
 *	op_add - function that return the sum of two numbers.
 *	@a: integer.
 *	@b: integer.
 *	Return: integer
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *	op_sub - function that return the sub of two numbers.
 *	@a: integer.
 *	@b: integer.
 *	Return: integer
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *	op_mul - function that return the multipl of two numbers.
 *	@a: integer.
 *	@b: integer.
 *	Return: integer
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *	op_div - function that return the division of two numbers.
 *	@a: integer.
 *	@b: integer.
 *	Return: integer
*/

int op_div(int a, int b)
{
	return (a / b);
}
/**
 *	op_mod - function that return the mod of two numbers.
 *	@a: integer.
 *	@b: integer.
 *	Return: integer
*/

int op_mod(int a, int b)
{
	return (a % b);
}
